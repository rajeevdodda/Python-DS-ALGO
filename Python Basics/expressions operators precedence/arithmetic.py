# Arithmetic Operators ==> +, -, *, /, // , %

print(1 + 2)  # results int
print(1. + 3)  # results float

print(4 / 2)  # results true division with floating point
print(22 / 7)

print(22 // 7)  # returns quotient ==> int

print(22 % 7)  # returns remainder ==> int

print(1.20 % 0.4)  # returns remainder in float (doesnt return 0)

print(1.2 // 0.4)  # returns remainder in float ( doesnt return 3)

print(27 // -4)  # returns -7
print(27 % -4)  # returns -1  (if divisor is negative then remainder should always be greater than divisor)


